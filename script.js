// Function to change language
function changeLanguage(lang) {
  // Update HTML direction
  document.documentElement.dir = lang === "ar" ? "rtl" : "ltr";

  // Update button states
  document.getElementById("lang-en").classList.toggle("active", lang === "en");
  document.getElementById("lang-ar").classList.toggle("active", lang === "ar");

  // Save to localStorage
  localStorage.setItem("resumeLang", lang);

  // Update all translatable elements
  const elements = document.querySelectorAll("[id]");
  elements.forEach((element) => {
    const key = element.id;
    if (translations[lang][key]) {
      element.textContent = translations[lang][key];
    }
  });

  // Update badge texts
  document.querySelectorAll(".badge").forEach((badge) => {
    if (badge.id === "certified-badge") {
      badge.textContent = translations[lang]["certified"];
    } else if (badge.id === "advanced-badge") {
      badge.textContent = translations[lang]["advanced"];
    } else if (badge.id === "learning-badge") {
      badge.textContent = translations[lang]["learning"];
    } else if (badge.id === "current-badge") {
      badge.textContent = translations[lang]["current"];
    } else if (badge.id === "years-exp") {
      badge.textContent = translations[lang]["yearsExp"];
    }
  });
}

// Initialize language from localStorage or default to English
const savedLang = localStorage.getItem("resumeLang") || "en";
changeLanguage(savedLang);

// Add event listeners to language buttons
document
  .getElementById("lang-en")
  .addEventListener("click", () => changeLanguage("en"));
document
  .getElementById("lang-ar")
  .addEventListener("click", () => changeLanguage("ar"));
